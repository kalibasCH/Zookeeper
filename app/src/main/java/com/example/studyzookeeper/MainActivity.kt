fun main() {
    println("Please enter the number of the habitat you would like to view:")
    var ex = 0
    while (ex == 0) {
        val camera = readLine()!!.toString()
        if (camera == "0") {
            println(animals[0])
            println("Please enter the number of the habitat you would like to view:")
        } else if (camera == "1") {
            println(animals[1])
            println("Please enter the number of the habitat you would like to view:")
        } else if (camera == "2") {
            println(animals[2])
            println("Please enter the number of the habitat you would like to view:")
        } else if (camera == "3") {
            println(animals[3])
            println("Please enter the number of the habitat you would like to view:")
        } else if (camera == "4") {
            println(animals[4])
            println("Please enter the number of the habitat you would like to view:")
        } else if (camera == "5") {
            println(animals[5])
            println("Please enter the number of the habitat you would like to view:")
        } else if (camera == "exit") {
            ex = 1
        }
    }
    println("See you later!")
}
